public class Comida {
    
    //Atributos
    private double pesoComida;

    //Construtor
    public Comida(double pesoComida) {
        setPesoComida(pesoComida);
    }

    //Getters and Setters
    public double getPesoComida() {
        return pesoComida;
    }

    public void setPesoComida(double pesoComida) {
        this.pesoComida = pesoComida;
    }
}
